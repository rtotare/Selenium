package Practice;

public class ArraySquareTriangle {

	public static void main(String[] args) {

		//Square
		System.out.println("Print square");
		
		for(int i=1; i<=5;i++)
		{
			for(int j=1; j<=5; j++)
			   {
			System.out.print("*"  +   " ");
				}
			System.out.println("");
		}
	
		//Triangle
		System.out.println(" Print triangle");
		
				for(int i=1; i<=5;i++)
				{
					for(int j=1; j<=i; j++)
					   {
					System.out.print(j +   " ");
						}
					System.out.println("");
				}
				
		
		
		
		
		
		
		
	}

}
